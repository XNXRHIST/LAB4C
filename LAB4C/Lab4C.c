/*Код до завдання№3 Лаб роботи №4 
* Обчислити: p = ∑(s), де s = ∏(j - i) для j = 1 до i, а i = 1 до 5.

*/
#include<stdio.h>//Підключаємо необхідні для роботи бібліотеки
#include <math.h>

int main() {
	int j, i, s, p = 0;//Оголошуємо змінні
	for (i = 1; i <= 5; i++)
	{
		//Обчислення факторіала числа i
		for (s = 1, j = 1; j <= i; j++) s *= j;

		//Сумування факторіалів
		p += s;
	}

	//Виведення значення p на екран юзера
	printf("p = %d\n", p);

	return 0;//Кінець програми
}
